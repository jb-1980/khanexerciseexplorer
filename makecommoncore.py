    def makecommoncore(self):
        self.get_grades()
               
        for grade in self.GRADES:
            self.get_strands(grade)
        
        for strand in self.STRANDS:
            self.get_standards(strand)

        self.close_browser()
        
        return self.STANDARDS

    def get_standards(self,strand):
        self.BROWSER.get(strand)
        skills = self.BROWSER.find_elements_by_class_name('standard-preview')
        for skill in skills:
            standard = self.clean_reactid(skill.get_attribute('data-reactid'))
            skill_data = {
                'category':self.CATEGORY_MAP[standard[0]],
                'strand':standard[1],
                'standard':standard[2],
                'skill':standard[3],
            }
            self.STANDARDS.append(skill_data)

    def get_strands(self,grade):
        self.BROWSER.get(grade)
        strands = self.BROWSER.find_elements_by_xpath('//ul[@class="third-tier"]//a')
        for strand in strands:
            self.STRANDS.append(strand.get_attribute('href'))
    
    def get_grades(self):
        url = 'https://www.khanacademy.org/commoncore/map'
        self.BROWSER.get(url)
        
        grades = self.BROWSER.find_elements_by_class_name('standard-preview')
        for grade in grades:
            self.GRADES.append(grade.get_attribute('href'))        
    
    def clean_reactid(self,reactid):
        data = reactid.split('$')
        skill = data[3]
        strand = data[1].split('.')[0]
        dirty_standard = data[2].split('.')[0].split('=1')
        clean_standard = ('.').join(dirty_standard)
        category = dirty_standard[0].split('-')[0]

        return (category,strand,clean_standard,skill)
    
    def close_browser(self):
        self.BROWSER.quit()
    
